#ifndef STD_TYPES_H
#define STD_TYPES_H

typedef unsigned char           u8;   // 1 byte
typedef unsigned short int      u16;  // 2 bytes
typedef unsigned long int       u32;  // 8 bytes
typedef unsigned long long int	u64;  // 8 bytes

typedef signed char 			s8;   // 1 byte
typedef signed short int 		s16;  // 2 bytes
typedef signed long int 		s32;  // 8 bytes
typedef signed long long int	s64;  // 8 bytes

typedef float		 			f32;  // 4 bytes
typedef double 			 		d64;  // 8 bytes
typedef long double		 		d80;  // 16 bytes

#endif
